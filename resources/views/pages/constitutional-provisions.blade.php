    <div class="bpsc-content">
        <h2 class="bpsc-section-title">Constitutional Provisions for Public Service Commissions</h2>

        <p>
            The Public Service Commissions for the Union and for the States are established and governed
            under Part XIV of the Constitution of India. Articles 315 to 323 deal with the provisions
            relating to the Public Service Commissions.
        </p>

        <div class="bpsc-article-block">
            <h3>Article 315 — Public Service Commissions for the Union and for the States</h3>
            <p>
                There shall be a Public Service Commission for the Union and a Public Service Commission for
                each State. Two or more States may agree that there shall be one Public Service Commission
                for that group of States, and an act of Parliament may provide for the appointment of a
                Joint State Public Service Commission (JSPSC) to serve the needs of those States.
            </p>
        </div>

        <div class="bpsc-article-block">
            <h3>Article 316 — Appointment and term of office of Members</h3>
            <p>
                The Chairman and other members of a Public Service Commission shall be appointed, in the
                case of the Union Commission or a Joint Commission, by the President, and in the case of a
                State Commission, by the Governor of the State. As nearly as may be one-half of the members
                of every Public Service Commission shall be persons who have held office for at least ten
                years either under the Government of India or under the Government of a State.
            </p>
            <p>
                A member of a Public Service Commission shall hold office for a term of six years from the
                date on which he enters upon his office or until he attains, in the case of the Union
                Commission, the age of sixty-five years, and in the case of a State Commission or a Joint
                Commission, the age of sixty-two years, whichever is earlier.
            </p>
        </div>

        <div class="bpsc-article-block">
            <h3>Article 317 — Removal and suspension of a member of a Public Service Commission</h3>
            <p>
                A member of a Public Service Commission shall only be removed from his office by order of
                the President on the ground of misbehaviour after the Supreme Court, on reference being
                made to it by the President, has, on inquiry held in accordance with the procedure
                prescribed in that behalf under Article 145, reported that the member ought on any such
                ground to be removed.
            </p>
        </div>

        <div class="bpsc-article-block">
            <h3>Article 318 — Power to make regulations as to conditions of service of members and staff</h3>
            <p>
                In the case of the Union Commission or a Joint Commission, the President and, in the case
                of a State Commission, the Governor of the State may by regulations— (a) determine the
                number of members of the Commission and their conditions of service; and (b) make provision
                with respect to the number of members of the staff of the Commission and their conditions
                of service.
            </p>
        </div>

        <div class="bpsc-article-block">
            <h3>Article 319 — Prohibition as to the holding of offices by members of Commission on ceasing to be such members</h3>
            <p>
                On ceasing to hold office — (a) the Chairman of the Union Public Service Commission shall
                be ineligible for further employment either under the Government of India or under the
                Government of a State; (b) the Chairman of a State Public Service Commission shall be
                eligible for appointment as the Chairman or any other member of the Union Public Service
                Commission or as the Chairman of any other State Public Service Commission, but not for
                any other employment either under the Government of India or under the Government of a State.
            </p>
        </div>

        <div class="bpsc-article-block">
            <h3>Article 320 — Functions of Public Service Commissions</h3>
            <p>
                It shall be the duty of the Union and the State Public Service Commissions to conduct
                examinations for appointments to the services of the Union and the services of the State
                respectively. It shall also be the duty of the Union Public Service Commission, if
                requested by any two or more States so to do, to assist those States in framing and
                operating schemes of joint recruitment for any services for which candidates possessing
                special qualifications are required.
            </p>
        </div>
